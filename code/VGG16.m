    for i=1:10
        axes(handles.(['axes',num2str(i)]));
        imshow(uint8(img(:,:,:,index_new(i))));
        set(handles.(['edit',num2str(i)]),'String',[index_new(i)]);
    end       
elseif pp==32
       set(handles.edit11,'String',['page ' num2str(pp) ' of 32']);

    for i=1:4
        axes(handles.(['axes',num2str(i)]));
        img(:,:,:,i)=imshow(uint8(img(:,:,:,index_new(i))));
        set(handles.(['edit',num2str(i)]),'String',[index_new(i)]);
    end
